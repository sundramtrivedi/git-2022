LEFT JOIN
(
    SELECT LMS_SUPPLIERS_DETAILS.SUPPLIER_ID, LMS_SUPPLIERS_DETAILS.SUPPLIER_NAME, LMS_SUPPLIERS_DETAILS.CONTACT, COUNT(*) FLAG
    FROM LMS_SUPPLIERS_DETAILS
    INNER  JOIN LMS_BOOK_DETAILS ON (LMS_SUPPLIERS_DETAILS.SUPPLIER_ID = LMS_BOOK_DETAILS.SUPPLIER_ID)
